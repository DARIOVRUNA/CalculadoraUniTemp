let from_Kelvin_To_Celcius =  (k) =>  k - 273.15
let from_Fahrenheit_To_Celcius = (f) => 5*(f - 32 )/9
let from_Celcius_To_Kelvin = (c) => c + 273.15
let from_Kelvin_To_Fahrenheit = (k) => 9*(k - 273.15) / 5 + 32
let from_Fahrenheit_To_Kelvin = (f) => 5*(f - 32) / 9 + 273.15
let from_Celcius_To_Fahrenheit = (c) => 9*c/ 5 + 32